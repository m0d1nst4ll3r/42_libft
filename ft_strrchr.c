char	*ft_strrchr(char *str, char c)
{
	int	i;

	if (! str)
		return (0);
	i = 0;
	while (str[i])
		i++;
	while (i >= 0)
		if (str[i--] == c)
			return (str + i + 1);
	return (0);
}
